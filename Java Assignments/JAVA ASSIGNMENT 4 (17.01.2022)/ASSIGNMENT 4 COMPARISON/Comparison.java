import java.util.Scanner;
public class Comparison {
 public static void main( String args[] )    
    {
        int num1=25;      
        int num2=50;		
      
        if ( num1 == num2 )           
            System.out.println(num1+"==" +num2 );  
		
        if ( num1 != num2 )          
            System.out.println(num1+"!=" +num2 ); 
		
        if ( num1 < num2 )          
            System.out.println (num1+"<" +num2 ); 
		
        if ( num1 > num2 )          
            System.out.println(num1+">" +num2);  
		
        if ( num1 <= num2 )          
            System.out.println(num1+"<=" +num2 );  
		
        if ( num1 >= num2 )          
            System.out.println(num1+">=" +num2 );  
		
    }
  }